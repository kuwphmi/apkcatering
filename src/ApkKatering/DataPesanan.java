    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
    try {
        String namaPaket = comboMenu.getSelectedItem().toString();
        int id_menu = mapIdMenu.get(namaPaket); // Ambil ID dari HashMap
        String isi = txtIsi.getText();
        int harga = Integer.parseInt(txtHarga.getText());
        int qty = Integer.parseInt(txtQty.getText());
        int subtotal = harga * qty;

        // Ambil tanggal dari JDateChooser
        java.util.Date utilTanggalPesan = txtTglPesan.getDate();
        java.util.Date utilTanggalKirim = txtTglAcara.getDate();

        if (utilTanggalPesan == null || utilTanggalKirim == null) {
            JOptionPane.showMessageDialog(this, "Tanggal pesan dan kirim harus diisi!");
            return;
        }

        java.sql.Date sqlTanggalPesan = new java.sql.Date(utilTanggalPesan.getTime());
        java.sql.Date sqlTanggalKirim = new java.sql.Date(utilTanggalKirim.getTime());

        // Koneksi ke database
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_katering", "root", "");
        String query = "INSERT INTO pesanan (id_menu, isi, harga, qty, subtotal, tanggal_pesan, tanggal_kirim) VALUES (?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(query);
        stmt.setInt(1, id_menu);
        stmt.setString(2, isi);
        stmt.setInt(3, harga);
        stmt.setInt(4, qty);
        stmt.setInt(5, subtotal);
        stmt.setDate(6, sqlTanggalPesan);
        stmt.setDate(7, sqlTanggalKirim);

        stmt.executeUpdate();
        stmt.close();
        conn.close();

        JOptionPane.showMessageDialog(this, "Pesanan berhasil disimpan!");

        // Reset form
        comboMenu.setSelectedIndex(0);
        txtIsi.setText("");
        txtHarga.setText("");
        txtQty.setText("");
        txtSubtotal.setText("");
        txtTglPesan.setDate(null);
        txtTglAcara.setDate(null);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Harga dan Qty harus berupa angka!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

