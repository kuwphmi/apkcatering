    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMenu.getSelectedRow();
        
        if (selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Pilih Data yang Ingin diUbah");
            return;
        }
        
        String id = tblMenu.getValueAt(selectedRow, 0).toString();
        String nama = txtNama.getText();
        String hargaStr = txtHarga.getText();
        
        if (nama.isEmpty() || hargaStr.isEmpty()){
            JOptionPane.showMessageDialog(this, "Nama dan Harga Wajib diIsi");
            return;
        }
        
        try{
            int harga = Integer.parseInt(hargaStr);
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_katering", "root", "");
            String query = "UPDATE menu SET nama=?, harga=? WHERE id=?";
            PreparedStatement stmt = conn.prepareStatement(query);
            
            stmt.setString(1, nama);
            stmt.setInt(2, harga);
            stmt.setInt(3, Integer.parseInt(id));
            
            int rowsUpdated = stmt.executeUpdate();
            stmt.close();
            conn.close();
            
            if (rowsUpdated > 0){
                JOptionPane.showMessageDialog(this, "Data Berhasil diUpdate");
            } else {
                JOptionPane.showMessageDialog(this, "Update Gagal, Data Tidak diTemukan");
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error:" + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

