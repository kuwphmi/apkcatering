    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMenu.getSelectedRow();
        
        if (selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Pilih Data yang Ingin diHapus");
            return;
        }
        
        String id = tblMenu.getValueAt(selectedRow, 0).toString();
        
        int confirm = JOptionPane.showConfirmDialog(this, "Yakin Ingin Menghapus Data ?","Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_katering", "root", "");
            String query = "DELETE FROM menu WHERE id=?";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setInt(1, Integer.parseInt(id));
            
            int rowsDeleted = stmt.executeUpdate();
            stmt.close();
            conn.close();
            
            if (rowsDeleted > 0){
                JOptionPane.showMessageDialog(this, "Data Berhasil diHapus");
                txtNama.setText("");
                txtHarga.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Hapus GAGAL, Data Tidak diTemukan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Errpr:"+e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed
    
