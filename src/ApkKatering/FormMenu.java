    private void btnTampilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTampilActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblMenu.getModel();
        model.setRowCount(0);
        
        try {
           Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_katering", "root", "");
           Statement stmt = conn.createStatement();
           ResultSet rs = stmt.executeQuery("SELECT * FROM menu");
           
           while (rs.next()) {
               model.addRow(new Object[]{
               rs.getInt("id"),
               rs.getString("nama"),
               rs.getInt("harga")
                });
           }
           rs.close();
           stmt.close();
           conn.close();
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error:" + e.getMessage());
        }
    }//GEN-LAST:event_btnTampilActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String nama = txtNama.getText();
        String hargaStr = txtHarga.getText();
        
        if (nama.isEmpty() || hargaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama dan Harga Wajin diIsi !");
            return;
        }
        
        try {
            int harga = Integer.parseInt(hargaStr);
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_katering", "root", "");
            String query = "INSERT INTO menu (nama, harga) VALUES (?, ?)";
             PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, nama);
            stmt.setString(2, hargaStr);
            stmt.executeUpdate();
            
            stmt.close();
            conn.close();
            
            JOptionPane.showMessageDialog(this, "Data Berhasil diTambahkan");
            txtNama.setText("");
            txtHarga.setText("");
            
            
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Harga Harus Berupa Angka");
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

